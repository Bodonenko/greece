		<h2>Фідій</h2>
		<p>
			Фі́дій (грец. Φειδίας) — давньогрецький скульптор 5 ст. до н. е.
			Один з найбільших майстрів давньогрецького мистецтва епохи високої класики.
			Серед робіт Фідія, відомих тільки за описами античних авторів та копіями, найбільш уславленою була колосальна бронзова статуя Афіни Промахос (грец. Πρόμαχος — Передової Воїтельниці), встановлена близько 460 до н. е. на афінському Акрополі в пам'ять про перемогу над персами — та двох грандіозних статуй,виконаних у техніці хрисоелефантійної скульптури: Зевса Олімпійського в храмі Зевса в Олімпії (одне з «семи чудес світу») і Афіни Парфенос (Діви) в храмі Парфенон в Афінах.
			Найкраще уявлення про творчість Фідія дає скульптурне оздоблення Парфенону в Афінах (мармур, 438–432 до н. е., зберігаються в музеях: Британський музей, Лондон; Лувр, Париж; Новий музей Акрополя, Афіни), виконане під керівництвом та за особистої участі майстра.
			Ці твори, повні глибокої людяності, прославляють велич афінського полісу та його громадян, відображають ідеї боротьби афінян проти їхніх ворогів.
			Скульптура Парфенону, що у поєднанні з архітектурою храму є одним з найвищих зразків синтезу мистецтв, відрізняється надзвичайним багатством ритмів та композиційних прийомів.
		</p>
