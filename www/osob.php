		</p>
		<p>
			Найвідомішим творінням Іктіна (спільно з Каллікратом) є Парфенон, зведений у період 447–438 до н. е. 
			Також він побудував Одеон Перикла в Афінах, храм Аполлона в Бассах (близько 430 до н. е.), де вперше з'являється коринфський капітель та коринфський ордер використовується в якості тектонічного декору. 
